public class ap1 {

    private  int aa=10;
    int bb=20;
    public int cc=30;
    protected int dd=40;

    public static void main(String[] args) {
        ap1 obj_a=new ap1();
        System.out.println(obj_a);
        System.out.println(obj_a.aa);
        System.out.println(obj_a.bb);
        System.out.println(obj_a.cc);
        System.out.println(obj_a.dd);

    }

}
